char		*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i - 1] == 32) || (str[i - 1] == 43) || \
				(str[i - 1] == 45) || (str[i - 1] == '\0'))
		{
			if (str[i] > 96 && str[i] < 123)
			{
				str[i] = str[i] - 32;
			}
		}
		i++;
	}
	return (0);
}
