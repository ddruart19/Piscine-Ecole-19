int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		i++;
	}
	while (s2[j])
	{
		j++;
	}
	if (i > j)
	{
		return (1);
	}
	if (i < j)
	{
		return (-1);
	}
	else
	{
		return (0);
	}
}
